package old;

public class Action {

	private String name;
	private String prevScreen;
	private String nextScreen;
	private String buttonName;
	private Double prevPrecision;
	private Double nextPrecision;
	private Double buttonPrecision;
	private Integer prevSkip;
	private Integer nextSkip;
	private Integer buttonSkip;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrevScreen() {
		return prevScreen;
	}
	public void setPrevScreen(String prevScreen) {
		this.prevScreen = prevScreen;
	}
	public String getNextScreen() {
		return nextScreen;
	}
	public void setNextScreen(String nextScreen) {
		this.nextScreen = nextScreen;
	}
	public String getButtonName() {
		return buttonName;
	}
	public void setButtonName(String buttonName) {
		this.buttonName = buttonName;
	}
	public Double getPrevPrecision() {
		return prevPrecision;
	}
	public void setPrevPrecision(Double prevPrecision) {
		this.prevPrecision = prevPrecision;
	}
	public Double getNextPrecision() {
		return nextPrecision;
	}
	public void setNextPrecision(Double nextPrecision) {
		this.nextPrecision = nextPrecision;
	}
	public Double getButtonPrecision() {
		return buttonPrecision;
	}
	public void setButtonPrecision(Double buttonPrecision) {
		this.buttonPrecision = buttonPrecision;
	}
	public Integer getPrevSkip() {
		return prevSkip;
	}
	public void setPrevSkip(Integer prevSkip) {
		this.prevSkip = prevSkip;
	}
	public Integer getNextSkip() {
		return nextSkip;
	}
	public void setNextSkip(Integer nextSkip) {
		this.nextSkip = nextSkip;
	}
	public Integer getButtonSkip() {
		return buttonSkip;
	}
	public void setButtonSkip(Integer buttonSkip) {
		this.buttonSkip = buttonSkip;
	}
	
	
}
